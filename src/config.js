/**
 * =============================================================================
 * 本文件里的英文缩写 / 字段名是什么意思
 * =============================================================================
 * config / defaultConfig   configuration 配置：默认的一组数字旋钮。
 * vocabSize                vocabulary size：词表里有多少个不同符号；这里占位 0，训练里会被真实值覆盖。
 * seqLen                   sequence length：一次看连续多少个 token（这里指字）。
 * dModel                   d=model dimension：「每个字的向量有多宽」；d 常表示 dimension（维度）。
 * nHeads                   n=数量，Heads=注意力头数：并行分几路看关系。
 * nLayers                  Layers=层数：Transformer 块堆几层。
 * dFf                      Ff=feed-forward 中间层宽度：先变宽到多宽再压回 dModel。
 * lr                       learning rate 学习率：每次改权重迈多大步。
 * steps                    训练循环执行多少次（多少步）。
 * seed                     随机种子：固定它可以让「随机初始化」可复现。
 *
 * =============================================================================
 * config.js —— 所有「旋钮」集中在一处，改这里就能改模型大小和训练节奏。
 * =============================================================================
 *
 * 【为什么要单独文件】训练脚本和以后实验只改数字，不用到处翻代码；
 *                        也方便你对比「只改 lr」和「只改层数」的效果。
 *
 * 【调大 / 调小的后果（直觉）】
 * - dModel、nLayers、dFf 变大：模型更能拟合复杂规律，但算得更慢、更容易要更多数据。
 * - seqLen 变大：每次看更多上下文，但矩阵更大（注意力是 T×T），慢很多。
 * - lr 太大：每次改表步子太大，「错多少」可能乱跳甚至算出异常；太小：要学很久才见效。
 * - steps 太少：还没学会就停了；太多：小语料上可能背过拟合。
 *
 * vocabSize 写 0 是占位：真实词表大小在 train.js 里用语料算出来再覆盖。
 */

export const defaultConfig = {
  vocabSize: 0,
  seqLen: 64,
  dModel: 64,
  nHeads: 4,
  nLayers: 2,
  dFf: 128,
  lr: 0.02,
  steps: 200,
  seed: 42,
};
