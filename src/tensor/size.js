/**
 * =============================================================================
 * 本文件里的英文词是什么意思
 * =============================================================================
 * prod             product 乘积：把几个数乘在一起（这里用于行×列=总格子数）。
 * dims             dimensions：维度列表，如 [行, 列]。
 * assertShape2D    assert=断言、shape=形状、2D=二维：检查二维形状是否和数组长度一致。
 * dataLen          data length：数组里实际有多少个数。
 * rows / cols      行数 / 列数。
 * shape mismatch   形状不匹配：说的行列和真实格子数对不上。
 *
 * =============================================================================
 * size.js —— 两件小事：算「表里有多少个格子」、检查「说的行列数」和「实际格子数」是否一致
 * =============================================================================
 *
 * 【问题】程序里把表格按行排成一长串小数存起来；若嘴上说 3 行 4 列，却只存了 10 个格子，
 *        后面按行按列去取数时就会错位，结果乱套，还很难发现。
 *
 * 【做法】
 * prod：把「行数 × 列数」算出来，应该等于那一长串格子的个数。
 * assertShape2D：不一致就立刻报错，逼你在入口处修掉。
 */

/** 把几行几列乘起来 = 一共有多少个格子。 */
export const prod = (dims) => dims.reduce((a, b) => a * b, 1);

/**
 * 检查：说好了 rows 行 cols 列，那一长串是不是正好 rows×cols 个格子。
 */
export const assertShape2D = (dataLen, rows, cols) => {
  if (rows * cols !== dataLen) {
    throw new Error(`shape mismatch: ${rows}x${cols} != ${dataLen}`);
  }
};
